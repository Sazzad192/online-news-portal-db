        var audio, playbtn, mutebtn, seekslider, seeking=false, seekto;

        function initAudioPlayer(){
            audio = new Audio();
            audio.src = 'https://www.podtrac.com/pts/redirect.mp3/feeds.soundcloud.com/stream/{{$podcasts->cloud_id}}-dpostonline.mp3';
            audio.loop = true;
            audio.play();
            
            // Set object references
            playbtn = document.getElementById("playpausebtn");
            seekslider = document.getElementById("seekslider");

            // Add Event Handling
            playbtn.addEventListener("click",playPause);
            seekslider.addEventListener("mousedown", function(event){ seeking=true; seek(event); });
            seekslider.addEventListener("mousemove", function(event){ seek(event); });
            seekslider.addEventListener("mouseup",function(){ seeking=false; });
            
            // Functions
            function playPause(){

                if(audio.paused){
                    audio.play();
                    playbtn.innerHTML ='<i class="fa fa-pause fa-1x"></i>';
                }
                
                else {
                    audio.pause();
                    playbtn.innerHTML = '<i class="fas fa-play fa-1x"></i>';
                }
            }

            function seek(event){
                if(seeking){
                    seekslider.value = event.clientX - seekslider.offsetLeft;
                    seekto = audio.duration * (seekslider.value / 100);
                    audio.currentTime = seekto;
                }
            }
        }

        window.addEventListener("load", initAudioPlayer);
    </script>

